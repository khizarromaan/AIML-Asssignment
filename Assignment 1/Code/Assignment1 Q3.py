a = 15
b = 4

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Floor Division:", a // b)
print("Modulo:", a % b)
print("Exponentiation:", a ** b)

'''
we use / for normal division with float value but when we use // it is floor division which drops the float value and gives out int value
'''